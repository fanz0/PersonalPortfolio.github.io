// Smeat Project
let singleProjectH3Smeat = document.querySelector('#smeat .title-programs h3')
let singleDescriptionSmeat = document.querySelector('#smeat .description')

singleProjectH3Smeat.addEventListener("click", () => {
    if (!singleDescriptionSmeat.classList.contains('disappear')) {
        singleDescriptionSmeat.classList.add('disappear')
    } else {
        singleDescriptionSmeat.classList.remove('disappear')
}
})

